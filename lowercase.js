let str = "Hello";  // Example string

// Convert to lowercase
let lp = str.toLowerCase();
console.log("Lowercase version:", lp);

// Get the character code of the first character
let char = str.charCodeAt(0);
console.log("Character code of the first character:", char);

// Get the character at the index 2
let letter = str.at(2);
console.log("Character at index 2:", letter);